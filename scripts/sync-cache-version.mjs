import fs from 'node:fs/promises';
import path from 'node:path';

const rootDir = path.resolve(import.meta.dirname, '..');
const packageJsonPath = path.join(rootDir, 'package.json');
const buildInfoPath = path.join(rootDir, 'src', 'generated', 'buildInfo.ts');
const swTemplatePath = path.join(rootDir, 'scripts', 'templates', 'sw.template.js');
const swOutputPath = path.join(rootDir, 'public', 'sw.js');

const packageJson = JSON.parse(await fs.readFile(packageJsonPath, 'utf8'));
const appVersion = String(packageJson.version || 'dev').trim();
const versionToken = appVersion.replace(/[^0-9A-Za-z]+/g, '-');

const appShellCacheName = `cookmoxs-v${versionToken}`;
const recipeCacheName = `cookmoxs-recipes-v${versionToken}`;

const buildInfoSource = `// Generated by scripts/sync-cache-version.mjs. Do not edit manually.\nexport const APP_BUILD_VERSION = '${appVersion}';\nexport const APP_SHELL_CACHE_NAME = '${appShellCacheName}';\nexport const RECIPE_CACHE_NAME = '${recipeCacheName}';\n`;

await fs.mkdir(path.dirname(buildInfoPath), { recursive: true });
await fs.writeFile(buildInfoPath, buildInfoSource, 'utf8');

const swTemplate = await fs.readFile(swTemplatePath, 'utf8');
const swSource = swTemplate
  .replaceAll('__APP_SHELL_CACHE_NAME__', appShellCacheName)
  .replaceAll('__RECIPE_CACHE_NAME__', recipeCacheName);

await fs.writeFile(swOutputPath, swSource, 'utf8');
